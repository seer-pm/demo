import { readContract } from "https://esm.sh/@wagmi/core@2.12.2";
import { COLLATERAL_TOKENS, SupportedChain, config } from "./config.ts";

export async function convertFromSDAI(amount: bigint, chainId: SupportedChain) {
  return await readContract(config, {
    allowFailure: false,
    address: COLLATERAL_TOKENS[chainId].primary.address as `0x${string}`,
    abi: SDAI_ABI,
    functionName: "convertToAssets",
    args: [amount],
    chainId: chainId,
  });
}

